def get_posture_vector(char):
    """
    获取单个汉字的书法姿态向量
    
    返回: dict with 8 dimensions (简化为5个可计算维度 + 3个占位)
    """
    strokes = get_stroke_count(char)
    radical, pos = detect_radical(char)
    
    # 笔画数越多 → 越"阴"（复杂、内敛）
    # 归一化到 [0, 1]，1-20划映射
    yin_from_strokes = min(strokes / 20.0, 1.0)
    
    # 基础阴阳来自部首
    base_yin_yang = 0.5  # 默认中性
    if radical and radical in RADICAL_YINYANG:
        base_yin_yang = RADICAL_YINYANG[radical]
    
    # 位置修正
    position_bias = 0.0
    if pos == 'left':
        position_bias = -0.08  # 左偏旁偏阴
    elif pos == 'top':
        position_bias = 0.05   # 上偏旁偏阳
    
    yin_yang = base_yin_yang + position_bias
    yin_yang = max(0.0, min(1.0, yin_yang))
    
    # 构建8维向量
    return {
        # 核心8维（书法用笔+测字结构）
        'qibi': yin_yang,            # 起笔：阴阳
        'shoubi': yin_yang - 0.05,   # 收笔：略收（回锋）
        'xingbi': 0.5 + (yin_yang - 0.5) * 0.7,  # 行笔：中锋倾向
        'xingtai': 0.3 + yin_yang * 0.4,  # 形态：圆笔→方笔
        'lidu': strokes / 20.0,      # 力度：笔画越多越"按"
        'sudu': 1.0 - strokes / 20.0, # 速度：笔画越多越"涩"
        'zhuanzhe': yin_yang,        # 转折：方折↔圆转
        'jiegou': yin_yang,          # 结构：内擫↔外拓
        
        # 补充4维（测字启发）
        'buwei_yinyang': base_yin_yang,  # 部首阴阳
        'kechaijie': 0.3 if radical else 0.7,  # 可拆解度
        'wanman': min(strokes / 10.0, 1.0),    # 完满度
        'bujian_puxi': base_yin_yang,           # 部件谱系
    }


def text_to_signal(text, target_len=100):
    """
    将一段中文文本转为数值信号
    
    Args:
        text: 中文字符串
        target_len: 输出信号长度（插值到）
    
    Returns:
        list of float: 信号值（综合阴阳）
    """
    chars = [c for c in text if '\u4e00' <= c <= '\u9fff' or c in STROKE_COUNT]
    if not chars:
        return [0.5] * target_len
    
    # 每个字提取姿态，取起笔阴阳作为简化的信号值
    signals = []
    for char in chars:
        posture = get_posture_vector(char)
        # 综合：起笔 + 行笔 + 结构 的平均
        value = (posture['qibi'] + posture['xingbi'] + posture['jiegou']) / 3.0
        signals.append(value)
    
    # 插值到目标长度
    if len(signals) >= target_len:
        step = len(signals) / target_len
        result = [signals[int(i * step)] for i in range(target_len)]
    else:
        result = []
        for i in range(target_len):
            idx = i * len(signals) / target_len
            lo = int(idx)
            hi = min(lo + 1, len(signals) - 1)
            frac = idx - lo
            result.append(signals[lo] * (1 - frac) + signals[hi] * frac)
    
    return result


def question_to_signal(question, target_len=100):
    """问题文本 → v94 兼容的信号列表"""
    return text_to_signal(question, target_len)
