def get_stroke_count(char):
    """获取笔画数，未知返回估计值"""
    if char in STROKE_COUNT:
        return STROKE_COUNT[char]
    # 未知字按 Unicode 码点估算（粗略）
    code = ord(char)
    if 0x4E00 <= code <= 0x9FFF:  # CJK统一汉字
        return 10  # 中位数估计
    return 5


